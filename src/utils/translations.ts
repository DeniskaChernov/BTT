export function formatPrice(price: number, currency: string = "сум"): string {
  // Простое форматирование без Intl для избежания ошибок
  const formatted = price.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ' ');
  return `${formatted} ${currency}`;
}

export function getPriceRange(productId: keyof typeof priceData, t: Translations): string {
  const priceInfo = priceData[productId];
  if (!priceInfo) return '';
  
  // Если есть точная цена
  if ('price' in priceInfo) {
    if (productId === 'rattanThread') {
      // Для ротанга показываем цену за кг с минимальной суммой
      return `${formatPrice(priceInfo.price, t.currency)}/кг (мин. ${priceInfo.minQuantity}кг = ${formatPrice(priceInfo.price * priceInfo.minQuantity, t.currency)})`;
    }
    return formatPrice(priceInfo.price, t.currency);
  }
  
  // Если есть диапазон цен
  return `${t.priceFrom} ${formatPrice(priceInfo.min, t.currency)}`;
}

// Получить точную ��ену товара
export function getProductPrice(productId: keyof typeof priceData): number {
  const priceInfo = priceData[productId];
  if (!priceInfo) return 0;
  
  if ('price' in priceInfo) {
    // Для ротанга возвращаем минимальную сумму (5кг)
    if (productId === 'rattanThread') {
      return priceInfo.price * priceInfo.minQuantity;
    }
    return priceInfo.price;
  }
  
  return priceInfo.min;
}

// Получить цену за единицу
export function getUnitPrice(productId: keyof typeof priceData): number {
  const priceInfo = priceData[productId];
  if (!priceInfo) return 0;
  
  if ('price' in priceInfo) {
    return priceInfo.price;
  }
  
  return priceInfo.min;
}

// Вычислить итоговую сумму с учетом количества
export function calculateItemTotal(productId: keyof typeof priceData, quantity: number, isRattan: boolean = false): number {
  const unitPrice = getUnitPrice(productId);
  
  if (isRattan) {
    // Для ротанга количество в кг
    return unitPrice * quantity;
  }
  
  // Для кашпо количество в штуках
  return unitPrice * quantity;
}